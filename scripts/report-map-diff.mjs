import fs from 'node:fs'
import path from 'node:path'

const root = process.cwd()
const mapPath = path.join(root, 'cli.js.map')
const outputPath = path.join(root, 'openspec/changes/cli-js-map-diff.md')
const normalizeSourcePath = source =>
  source.replace(/^(?:\.\.\/)+/, '').replace(/^\/+/, '')

const map = JSON.parse(fs.readFileSync(mapPath, 'utf8'))
const replacement = String.fromCharCode(0xfffd)
const mojibakeHints = ['鈻', '鉁', '路']

/** @type {Array<{path:string,status:string,reason:string,localLength?:number,mapLength?:number}>} */
const rows = []

for (let i = 0; i < map.sources.length; i += 1) {
  const src = map.sources[i]
  const content = map.sourcesContent?.[i]
  if (!src || !content) continue
  if (!/\.(ts|tsx)$/.test(src)) continue

  const normalized = normalizeSourcePath(src)
  if (!normalized.startsWith('src/')) continue
  const filePath = path.join(root, normalized)
  if (!fs.existsSync(filePath)) {
    rows.push({
      path: normalized,
      status: 'missing_local',
      reason: 'file missing locally',
    })
    continue
  }

  const local = fs.readFileSync(filePath, 'utf8')
  if (local === content) continue

  const normalizedEqual =
    local.replace(/\r\n/g, '\n') === content.replace(/\r\n/g, '\n')
  const hasReplacement = local.includes(replacement)
  const hasMojibake = mojibakeHints.some(token => local.includes(token))

  rows.push({
    path: normalized,
    status: normalizedEqual ? 'line_ending_only' : 'differs',
    reason: normalizedEqual
      ? 'line ending only'
      : hasReplacement
        ? 'contains replacement char'
        : hasMojibake
          ? 'contains mojibake-like glyphs'
          : 'content differs',
    localLength: local.length,
    mapLength: content.length,
  })
}

const summary = {
  totalDiffs: rows.length,
  missing: rows.filter(row => row.status === 'missing_local').length,
  lineEndingOnly: rows.filter(row => row.status === 'line_ending_only').length,
  withReplacement: rows.filter(
    row => row.reason === 'contains replacement char',
  ).length,
  withMojibake: rows.filter(
    row => row.reason === 'contains mojibake-like glyphs',
  ).length,
  contentDiff: rows.filter(row => row.reason === 'content differs').length,
}

const diffRows = rows
  .filter(row => row.status !== 'line_ending_only')
  .sort((a, b) => a.path.localeCompare(b.path))
  .map(
    row =>
      `| \`${row.path}\` | \`${row.status}\` | ${row.reason} | ${row.localLength ?? '-'} | ${row.mapLength ?? '-'} |`,
  )

const report = [
  '# cli.js.map Diff Inventory',
  '',
  'Generated by `node scripts/report-map-diff.mjs`.',
  '',
  'This report compares every local `src/**/*.ts` and `src/**/*.tsx` file against the matching `sourcesContent` entry in `cli.js.map`.',
  '',
  '## Summary',
  '',
  `- Total diffs: ${summary.totalDiffs}`,
  `- Missing local files: ${summary.missing}`,
  `- Line-ending-only diffs: ${summary.lineEndingOnly}`,
  `- Diffs with replacement chars: ${summary.withReplacement}`,
  `- Diffs with mojibake-like glyphs: ${summary.withMojibake}`,
  `- Other content diffs: ${summary.contentDiff}`,
  '',
  '## Interpretation',
  '',
  summary.totalDiffs === 0
    ? '- Current local TS sources match `cli.js.map` exactly. Remaining `tsc` failures are not recoverable by simply restoring map content.'
    : '- Rows below are candidates for map-backed restoration. Prefer restoring map content before attempting manual type repairs.',
  '',
  '## Diff Table',
  '',
  '| Path | Status | Reason | Local Length | Map Length |',
  '| --- | --- | --- | ---: | ---: |',
  ...(diffRows.length > 0
    ? diffRows
    : ['| _none_ | `match` | Local source matches `cli.js.map` | - | - |']),
  '',
].join('\n')

fs.writeFileSync(outputPath, report, 'utf8')
console.log(report)
